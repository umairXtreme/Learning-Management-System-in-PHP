$users = $conn->query("SELECT id, full_name FROM users WHERE (role != 'admin' OR role IS NULL OR role = '')");

$editMode = isset($_GET['id']);
$enrollment = [
    'user_id' => '',
    'course_id' => '',
    'status' => 'Active',
    'progress' => 0,
    'enrolled_at' => date('Y-m-d\TH:i'),
    'completed_at' => ''
];
$errors = [];
