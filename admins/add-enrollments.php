// Restore invoice after redirect
if (isset($_SESSION['invoice_path'])) {
    $invoice_path = $_SESSION['invoice_path'];
    unset($_SESSION['invoice_path']);
}

if ($editMode) {
    $id = intval($_GET['id']);
    $stmt = $conn->prepare("SELECT * FROM enrollments WHERE id = ? AND is_deleted = 0");
    $stmt->bind_param("i", $id);
    $stmt->execute();
    $result = $stmt->get_result();
    if ($result->num_rows < 1) {
        die("Enrollment not found.");
    }
    $enrollment = $result->fetch_assoc();
}

$users = $conn->query("SELECT id, full_name FROM users WHERE role IN ('student', 'instructor') OR role IS NULL OR role = ''");
$courses = $conn->query("SELECT id, title, price FROM courses WHERE status = 'Published'");

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $user_id = intval($_POST['user_id']);
    $course_id = intval($_POST['course_id']);
    $status = $_POST['status'];
    $progress = intval($_POST['progress']);
    $enrolled_at = $_POST['enrolled_at'];
    $completed_at = $_POST['completed_at'];
    $price = "0.00";

    if ($user_id < 1 || $course_id < 1 || $progress < 0 || $progress > 100 || !in_array($status, ['Active', 'Completed', 'Dropped', 'Pending'])) {
        $errors[] = "Invalid input. Please verify fields.";
    }

    if (!$enrolled_at) $enrolled_at = date('Y-m-d H:i:s');

    // Check duplicate only on new insert
    if (!$editMode) {
        $checkDup = $conn->prepare("SELECT id FROM enrollments WHERE user_id = ? AND course_id = ? AND is_deleted = 0");
        $checkDup->bind_param("ii", $user_id, $course_id);
        $checkDup->execute();
        if ($checkDup->get_result()->num_rows > 0) {
            $errors[] = "❌ This user is already enrolled in the selected course.";
        }
        $checkDup->close();
    }

    if (empty($errors)) {
        if ($editMode) {
            $stmt = $conn->prepare("UPDATE enrollments SET user_id=?, course_id=?, status=?, progress=?, enrolled_at=?, completed_at=? WHERE id=?");
            $stmt->bind_param("iissssi", $user_id, $course_id, $status, $progress, $enrolled_at, $completed_at, $id);
        } else {
            $enrollment_id = sprintf("ENR%s%s%s%s", date("Y"), $course_id, $user_id, rand(1000, 9999));
            $stmt = $conn->prepare("INSERT INTO enrollments (user_id, course_id, enrollment_id, status, progress, enrolled_at, completed_at) VALUES (?, ?, ?, ?, ?, ?, ?)");
            $stmt->bind_param("iississ", $user_id, $course_id, $enrollment_id, $status, $progress, $enrolled_at, $completed_at);

            $pay_stmt = $conn->prepare("INSERT INTO payments (user_id, course_id, enrollment_id, amount, status, transaction_date) VALUES (?, ?, ?, ?, ?, NOW())");
            $pay_status = 'success';
            $pay_stmt->bind_param("iisss", $user_id, $course_id, $enrollment_id, $price, $pay_status);
        }

        if ($stmt->execute()) {
            if (!$editMode) {
                $pay_stmt->execute();
                $courseRes = $conn->query("SELECT title FROM courses WHERE id = $course_id");
                $course = $courseRes->fetch_assoc();
                $invoice_path = generateInvoice($course['title'], $user_id, $enrollment_id, $price, $status);
                $_SESSION['invoice_path'] = $invoice_path;
            }

            $_SESSION['success'] = $editMode ? "✅ Enrollment updated." : "✅ User enrolled successfully.";
            header("Location: " . $_SERVER['PHP_SELF']);
            exit();
        } else {
            $errors[] = "Database error: " . $stmt->error;
        }
    }
}

function generateInvoice($course_title, $user_id, $enrollment_id, $amount, $status) {
    $amount = number_format($amount, 2);
    $date = date("Y-m-d H:i");
    $html = <<<HTML
<style>
    body { font-family: DejaVu Sans; font-size: 14px; }
    h2 { color: #4CAF50; text-align: center; }
    .row { margin-bottom: 6px; }
</style>
<h2>Enrollment Invoice</h2>
<div class="row"><strong>Enrollment ID:</strong> $enrollment_id</div>
<div class="row"><strong>User ID:</strong> $user_id</div>
<div class="row"><strong>Course:</strong> $course_title</div>
<div class="row"><strong>Status:</strong> $status</div>
<div class="row"><strong>Amount:</strong> \$$amount</div>
<div class="row"><strong>Date:</strong> $date</div>
<hr>
<p style="font-size:12px;">This invoice was auto-generated by admin enrollment panel.</p>
HTML;

    $options = new Options();
    $options->set('isRemoteEnabled', true);
    $dompdf = new Dompdf($options);
    $dompdf->loadHtml($html);
    $dompdf->setPaper('A4', 'portrait');
    $dompdf->render();

    $file = "../courses/invoices/invoice-{$enrollment_id}.pdf";
    file_put_contents($file, $dompdf->output());

    return $file;
}
